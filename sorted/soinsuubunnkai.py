# 素因数分解

# sympyで簡単に素因数分解ができる
import sympy


# 約数を出せる
print(sympy.divisors(7776))

# 実行結果
# [1, 2, 3, 4, 6, 8, 9, 12, 16, 18, 24, 27, 32, 36, 48, 54, 72, 81, 96, 108, 144, 162, 216, 243, 288, 324, 432, 486, 648, 864, 972, 1296, 1944, 2592, 3888, 7776]

# 素因数分解できる
print(sympy.factorint(7776))

# 実行結果
# {2: 5, 3: 5}